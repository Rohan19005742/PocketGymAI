/**
 * Get all exercises
 */
export const getAllExercises = (): ExerciseData[] => exercisesData;

/**
 * Get exercises by category (primary muscle)
 */
export const getExercisesByCategory = (
  category: MuscleGroup | string
): ExerciseData[] => exercisesData.filter((ex) => ex.metadata.primaryMuscle === category);

/**
 * Get exercise by ID
 */
export const getExerciseById = (id: number): ExerciseData | undefined => {
  return exercisesData.find((ex) => ex.metadata.id === id);
};

/**
 * Search exercises by name
 */
export const searchExercises = (query: string): ExerciseData[] => {
  const lowerQuery = query.toLowerCase();
  return exercisesData.filter((ex) =>
    ex.metadata.name.toLowerCase().includes(lowerQuery)
  );
};
